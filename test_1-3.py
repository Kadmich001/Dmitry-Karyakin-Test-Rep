# 1. Составить алгоритм: если введенное число больше 7, то вывести “Привет”"""

while True: #запускаем бесконечный цикл
    try: #отлавливаем исключения
        chislo = int(input("Введите число:"))
        if chislo > 7:   #проверяем подходит ли под условие
            print("Привет")
            break #выход из цикла
        else:
            print("Введите другое число!") #если условие "число больше 7" не подходит
    except ValueError as e: #если ошибка выводим сообщение.
        print("Ошибка ввода! Введите числовое значение!")


