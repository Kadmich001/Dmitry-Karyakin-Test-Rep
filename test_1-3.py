"""Задание 3:
Составить алгоритм: на входе есть числовой массив, необходимо вывести элементы массива кратные 3 """
while True:
    try:  # отлавливаем исключения
        spisok = list(map(int, (input("Введите массив чисел через пробел:").split()))) # преобразуем строку в масив - элементы масива делаем числами - создаём список из масива
        krat_3 = [] #объявляем пустой список
        if len(spisok) > 0: # защита от пустого ввода
            for num in spisok: # перебираем элементы списка
                if num % 3 == 0: # проверяем элемент на кратность 3м
                    krat_3.append(str(num)) #добавляем элемент в числа кратные 3-м
            if len(krat_3) > 0: # провера наличия кратных элементов
                print(" ".join(krat_3)) #объединяем все строки в одну строку
                break
            else:
                print("Элементы массива кратные 3 не найдены!")
        else:
            print("Ошибка! Введен пустой список")
    except ValueError as e:  # если ошибка выводим сообщение.
        print("Ошибка ввода! Введите массив из числовых значений!")
